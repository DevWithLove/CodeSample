@interface WatchListViewController () {
    NSMutableArray *coursesToWatch; // used to store the unarchived objects
}

@end

@implementation WatchListViewController

- (void)viewWillAppear:(BOOL)animated {
    
    // Unarchive the watch list from the data file whenever this
    // view is about to be shown.
    
    // get the path to the data file
    NSURL *dataFile = [FileSystemHelper pathForDocumentsFile:@"watchlist.data"];
    
    // unarchive the contents of the data file
    coursesToWatch = [NSKeyedUnarchiver unarchiveObjectWithFile:[dataFile path]];
    
    // refresh the table view with the archived objects
    [[self tableView] reloadData];
}


